#include <stdio.h>
#include <math.h>
int main()
 {
 int a,b,c;
 printf("sides of the triangle=");
 scanf("%d""%d""%d",&a,&b,&c);
 if(a=b=c)
 {
 	printf("the triangle is equilateral");
 }
 else if(a=b!=c)
 {
 	printf("the triangle is isosceles");
 }
 else 
 { printf("the triangle is scalene");
 }
 
 	
 	
 	
 	
return 0;	
 }
